package com.maxent.oms.monitor.model;

public class TriggerConfig {

	private String tplTriggerId;
	private String description;
	private String params;
	private String expresss;

	public String getTplTriggerId() {
		return tplTriggerId;
	}

	public void setTplTriggerId(String tplTriggerId) {
		this.tplTriggerId = tplTriggerId;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getParams() {
		return params;
	}

	public void setParams(String params) {
		this.params = params;
	}

	public String getExpresss() {
		return expresss;
	}

	public void setExpresss(String expresss) {
		this.expresss = expresss;
	}

}
